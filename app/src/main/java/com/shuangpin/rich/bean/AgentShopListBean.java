public class AgentShopListBean {

    private String id;
    private String name;
    private String county;
    private String toReferrer;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getCounty() {
        return county;
    }

    public void setCounty(String county) {
        this.county = county;
    }

    public String getToReferrer() {
        return toReferrer;
    }

    public void setToReferrer(String toReferrer) {
        this.toReferrer = toReferrer;
    }
}
