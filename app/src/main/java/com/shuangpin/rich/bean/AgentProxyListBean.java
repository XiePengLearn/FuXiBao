public class AgentProxyListBean {

    private String id;
    private String name;
    private String mobile;
    private String toReferrer;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getToReferrer() {
        return toReferrer;
    }

    public void setToReferrer(String toReferrer) {
        this.toReferrer = toReferrer;
    }
}
